                    <h5 class="alt-font text-extra-dark-gray font-weight-600 w-85 margin-30px-bottom xl-w-100">About Scindia Kanya Vidyalaya</h5>
    
                    <p>Scindia Kanya Vidyalaya SKV is an all-girls' residential school in India - located in Gwalior- a historical city of Madhya Pradesh. About 300 Km south of Delhi, it is well connected with all major cities of the country.</p>
                    <p>The school was founded by Late Rajmata of Gwalior Srimant Vijaya Raje Scindia in 1956 with a view to educating the girl child in the context of newly independent India. It embodies her dream of providing progressive modern education and equipping young girls to be good citizens. Inaugurated by the then President of India, H E Dr Rajendra Prasad, the school took off on its mission to stay grounded to roots of tradition and to take wing in the exhilarating air of freedom.</p>
                    <p>The entry level class at SKV is standard VI. In today's competitive world, the need of the hour is to reduce the stress level of the students and at the same time increase their creativity. The school tries to develop in our students the ability to 'create' as against 'copy' by providing an experiential learning curriculum in the formative years. It not only enhances their learning potentials but also brings them to the center stage of learning. It facilitates the students taking charge of their own learning as well as making the content relevant to them.</p>
                    <p>SKV ranks among one of the best boarding schools in India and is affiliated to the Central Board of Secondary Education New Delhi. The medium of instruction is English. The subjects taught at +2 level include English, Hindi, Sanskrit, History, Geography, Sociology, Political Science, Painting, Music -Vocal as well as Instrumental, Dance- Kathak and Manipuri style, Psychology, Physical Education, Physics, Chemistry, Mathematics, Biology, Bio-Tech, Home Science, C++, Web Page Media, Business Studies, Accountancy, Economics and Entrepreneurship. The school follows a system of CCE - Continuous Comprehensive Evaluation for classes VI - X where a student's performance is assessed throughout the academic session on the basis of Formative and Summative assessments.</p>
                    <p>Our vision and mission is to offer holistic development of students by providing them with world class campus facilities and infrastructure that caters to the modern educational and extracurricular needs of students. Through various events at SKV, students are encouraged to participate and showcase their talents. This helps in motivating and moulding young students to explore their talents and transform themselves into independent and learned individuals.</p>
    
    
